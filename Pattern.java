//Patterns using single print statement


public class Pattern {
    public static void main(String[] args) {

        //Right half pyramid
        System.out.println("*\n* *\n* * *\n* * * *\n* * * * *\n");

        System.out.println("");

        //Reverse right half pyramid
        System.out.println("* * * * *\n* * * *\n* * *\n* *\n*\n"); 

        System.out.println("");

        //Left half pyramid
        System.out.println("        *\n      * *\n    * * *\n  * * * *\n* * * * *\n");     
    }
}
