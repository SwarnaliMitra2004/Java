//Patterns using print statement

class Pattern{
    public static void main(String[] args) {

        //Left Sided Triangle
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *"); 
        System.out.println("* * * * *");

        System.out.println("");

        //Inverted Triangle
        System.out.println("* * * * *"); 
        System.out.println("* * * *");   
        System.out.println("* * *"); 
        System.out.println("* *");
        System.out.println("*");

        System.out.println("");

        //Right Sided Triangle
        System.out.println("        *");
        System.out.println("      * *");
        System.out.println("    * * *");
        System.out.println("  * * * *"); 
        System.out.println("* * * * *");        
    }
}